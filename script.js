// Открытие модального окна для картинок и видео
function openModal(mediaSrc, isVideo = false, isVertical = false) {
    const modal = document.getElementById('imageModal');
    const modalContent = document.getElementById('fullImage');

    modal.style.display = 'flex';

    if (isVideo) {
        // Если это видео, создаем видео-элемент
        const video = document.createElement('video');
        video.src = mediaSrc;
        video.controls = true;
        video.autoplay = true;
        video.classList.add('modal-content');

        if (isVertical) {
            video.classList.add('vertical-video'); // Добавляем класс для вертикального видео
        }

        modalContent.replaceWith(video);
        video.id = 'fullImage'; // Заменяем старый ID
    } else {
        // Если это картинка, просто меняем src
        modalContent.src = mediaSrc;
        modalContent.classList.remove('vertical-video'); // Убираем класс, если это не видео
    }
}

// Обработчик закрытия
document.querySelector('.close').addEventListener('click', function () {
    document.getElementById('imageModal').style.display = 'none';
    const modalContent = document.getElementById('fullImage');

    // Если видео, удаляем его (чтобы при повторном открытии не было проблем)
    if (modalContent.tagName === 'VIDEO') {
        modalContent.remove();
        const img = document.createElement('img');
        img.id = 'fullImage';
        img.classList.add('modal-content');
        document.querySelector('.modal').appendChild(img);
    }
});

// Закрытие по ESC
